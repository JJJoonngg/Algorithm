/*
시간제한 2초
문제
N개의 정수 A[1], A[2], …, A[N]이 주어져 있을 때, 이 안에 X라는 정수가 존재하는지 알아내는 프로그램을 작성하시오.

입력
첫째 줄에 자연수 N(1≤N≤100,000)이 주어진다. 다음 줄에는 N개의 정수 A[1], A[2], …, A[N]이 주어진다.
다음 줄에는 M(1≤M≤100,000)이 주어진다. 다음 줄에는 M개의 수들이 주어지는데, 이 수들이 A안에 존재하는지 알아내면 된다. 모든 정수들의 범위는 int 로 한다.
5
4 1 5 2 3
5
1 3 7 9 5
출력
M개의 줄에 답을 출력한다. 존재하면 1을, 존재하지 않으면 0을 출력한다.
1
1
0
0
1
*/
#include <cstdio>
#include <vector>
#include <algorithm>
using namespace std;
vector<int> v;
int N, M;
void bfs(int start, int end, int data) {
	int mid = (start + end) / 2;
	if (start > end) {
		printf("0\n");
	}
	else {
		if (v[mid] == data) {
			printf("1\n");
			return;
		}
		else if (data > v[mid]) {
			start = mid + 1;
			bfs(start, end, data);
		}
		else if (data < v[mid]) {
			end = mid - 1;
			bfs(start, end, data);
		}
	}
}
int main() {
	
	scanf("%d", &N);
	for (int i = 0; i < N; i++) {
		int tmp;
		scanf("%d", &tmp);
		v.push_back(tmp);
	}
	sort(v.begin(), v.end());

	scanf("%d", &M);
	for (int i = 0; i < M; i++) {
		int data;
		scanf("%d", &data);
		bfs(0, N - 1, data);
	}
	return 0;
}
