#include <cstdio>
#include <stdbool.h>
using namespace std;
#define MAX 101010
typedef struct {
	int key;
} element;
typedef struct {
	element heap[MAX];
	int heap_size;
}HeapType;

void init(HeapType *h) {
	h->heap_size = 0;
}
bool is_empty(HeapType *h) {
	if (h->heap_size == 0) return true;
	else return false;
}

void insertMaxHeap(HeapType *h, element	item) {
	int i;
	i = ++(h->heap_size);

	while ((i != 1) && (item.key > h->heap[i / 2].key)) {
		h->heap[i] = h->heap[i / 2];
		i /= 2;
	}
	h->heap[i] = item;
}

element deleteMaxHeap(HeapType *h) {
	int parent, child;
	element item, tmp;
	item = h->heap[1];
	tmp = h->heap[(h->heap_size)--];
	parent = 1;
	child = 2;
	while (child <= h->heap_size) {
		if ((child < h->heap_size) && (h->heap[child].key < h->heap[child + 1].key))
			child++;
		if (tmp.key >= h->heap[child].key)break;
		h->heap[parent] = h->heap[child];
		parent = child;
		child *= 2;
	}
	h->heap[parent] = tmp;
	return item;
}

int main() {
	HeapType heap;
	init(&heap);
	int N;
	scanf("%d", &N);
	for (int i = 0; i < N; i++) {
		int x;
		scanf("%d", &x);
		if (x != 0) {
			element e;
			e.key = x;
			insertMaxHeap(&heap, e);
		}
		else if ((x == 0) && (!is_empty(&heap))) {
			printf("%d\n", deleteMaxHeap(&heap));
		}
		else if ((x == 0) && (is_empty(&heap))) {
			printf("0\n");
		}
	}
	return 0;
}